print("LIST\n")

hp = ["kucing","anjing","kelinci","domba"]

hp.append("ikan")
hp.insert(2,"burung")
hp.remove("domba")

print("Daftar Hewan Peliharaan:\n",hp)
#analisis
#diketahui inputan data yaitu kucing, anjing, kelinci, domba. Kemudian hp.append("ikan") maksudnya untuk menambahkan ikan di akhir list. Lalu, hp.insert(2,"burung") yaitu untuk membahkan kata burung setelah kata kedua yaitu setelah kata "anjing". Untuk hp.remove("domba") yaitu menghapus kata domba dalam list. Sehingga outputnya akan menghasilkan kucing, anjing, burung, kelinci, ikan.
